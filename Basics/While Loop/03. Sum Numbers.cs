using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace t03.SumNumbers
{
    class Program
    {
        static void Main()
        {
            int input = int.Parse(Console.ReadLine());
            int sum = 0;

            while (sum<input)
            {
                int currentInput = int.Parse(Console.ReadLine());
                sum += currentInput;
            }
            Console.WriteLine(sum);
        }
    }
}
